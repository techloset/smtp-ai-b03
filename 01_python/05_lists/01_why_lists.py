# 01_why_lists.py - Why do we need lists?

# WITHOUT list: messy, can't loop or scale
student1 = "Ali"
student2 = "Sara"
student3 = "Reza"
print("Without list:", student1, student2, student3)

# WITH list: one variable, easy to manage
students = ["Ali", "Sara", "Reza"]
print("With list:", students)

# Real-world uses:
cart = ["Laptop", "Mouse"]       # Shopping items
scores = [85, 90, 78]            # Test scores
temps = [22.5, 24.0, 21.8]       # Temperatures
